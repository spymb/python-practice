# =============================================================
# 练习 2.7：删除人名中的空白
# 在名字前后添加空白字符，分别使用 lstrip()、rstrip()、strip()
# =============================================================
name = "\t  \nJohn Doe  \t\n"
print(f"原始名字（含空白）: {repr(name)}")
print(f"lstrip() 处理后 : {repr(name.lstrip())}")
print(f"rstrip() 处理后 : {repr(name.rstrip())}")
print(f"strip() 处理后  : {repr(name.strip())}")
print("—" * 50)


